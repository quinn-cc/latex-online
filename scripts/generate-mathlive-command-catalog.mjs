import fs from "node:fs/promises";
import path from "node:path";
import { fileURLToPath } from "node:url";

const SCRIPT_DIR = path.dirname(fileURLToPath(import.meta.url));
const ROOT_DIR = path.resolve(SCRIPT_DIR, "..");
const SOURCE_PATH = path.join(ROOT_DIR, "vendor", "mathlive", "mathlive.mjs");
const OUTPUT_PATH = path.join(
  ROOT_DIR,
  "src",
  "editor",
  "prosemirror",
  "math-extensions",
  "mathlive-command-catalog.js"
);

function extractCommandNames(source) {
  const names = new Set();

  for (const match of source.matchAll(
    /defineFunction\((\[[\s\S]*?\]|"(?:\\.|[^"])*")\s*,/g
  )) {
    const rawNames = match[1];

    if (rawNames.startsWith("[")) {
      for (const nameMatch of rawNames.matchAll(/"((?:\\.|[^"])*)"/g)) {
        names.add(nameMatch[1]);
      }

      continue;
    }

    names.add(JSON.parse(rawNames));
  }

  for (const match of source.matchAll(/\["\\\\([A-Za-z][A-Za-z*]*)"\s*,/g)) {
    names.add(match[1]);
  }

  return [...names]
    .filter((name) => /^[A-Za-z][A-Za-z*]*$/.test(name))
    .sort((left, right) => left.localeCompare(right));
}

function buildModuleSource(commandNames) {
  return `// Generated by scripts/generate-mathlive-command-catalog.mjs
// Source: vendor/mathlive/mathlive.mjs

export const MATHLIVE_COMMAND_CATALOG = Object.freeze(${JSON.stringify(
    commandNames,
    null,
    2
  )});
`;
}

const source = await fs.readFile(SOURCE_PATH, "utf8");
const commandNames = extractCommandNames(source);
await fs.writeFile(OUTPUT_PATH, buildModuleSource(commandNames));

console.log(
  `Generated ${commandNames.length} MathLive command names at ${path.relative(
    ROOT_DIR,
    OUTPUT_PATH
  )}`
);
